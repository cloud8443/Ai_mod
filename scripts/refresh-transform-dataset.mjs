#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';

const root = path.resolve(process.cwd());
const mappingsDir = path.join(root, 'src/lib/transform/mappings');
const outFile = path.join(root, 'src/lib/transform/generatedMappings.ts');

const [importsRaw, classesRaw, methodsRaw] = await Promise.all([
  fs.readFile(path.join(mappingsDir, 'imports.json'), 'utf8'),
  fs.readFile(path.join(mappingsDir, 'classes.json'), 'utf8'),
  fs.readFile(path.join(mappingsDir, 'methods.json'), 'utf8')
]);

const imports = JSON.parse(importsRaw);
const classes = JSON.parse(classesRaw);
const methods = JSON.parse(methodsRaw);

const banner = `import type { ModLoader } from '../types/contracts';\n\nexport type MappingScope = {\n  sourceLoader?: Exclude<ModLoader, 'unknown'>;\n  targetLoader?: Exclude<ModLoader, 'unknown'>;\n  sourceVersionRange?: string;\n  targetVersionRange?: string;\n};\n\nexport type ImportRenameMapping = MappingScope & {\n  id: string;\n  from: string;\n  to: string;\n};\n\nexport type ClassRenameMapping = MappingScope & {\n  id: string;\n  from: string;\n  to: string;\n};\n\nexport type MethodRenameMapping = MappingScope & {\n  id: string;\n  className?: string;\n  from: string;\n  to: string;\n};\n\n// Generated by scripts/refresh-transform-dataset.mjs. Do not edit manually.\n`;

const body = [
  `export const IMPORT_RENAME_MAPPINGS: ImportRenameMapping[] = ${JSON.stringify(imports, null, 2)};`,
  `export const CLASS_RENAME_MAPPINGS: ClassRenameMapping[] = ${JSON.stringify(classes, null, 2)};`,
  `export const METHOD_RENAME_MAPPINGS: MethodRenameMapping[] = ${JSON.stringify(methods, null, 2)};`,
  ''
].join('\n\n');

await fs.writeFile(outFile, `${banner}${body}`);
console.log(`Updated ${path.relative(root, outFile)} from JSON mapping datasets.`);
